package org.example.response.detail;

public class BookToken {
  private String value;

  // Constructor
  public BookToken() {
  }

  public BookToken(String value) {
    this.value = value;
  }

  // Getter and Setter
  public String getValue() {
    return value;
  }

  public void setValue(String value) {
    this.value = value;
  }

}
